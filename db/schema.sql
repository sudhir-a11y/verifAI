CREATE EXTENSION IF NOT EXISTS pgcrypto;

DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'user_role') THEN
        CREATE TYPE user_role AS ENUM ('super_admin', 'doctor', 'user', 'auditor');
    END IF;

    IF EXISTS (SELECT 1 FROM pg_type WHERE typname = 'user_role')
       AND NOT EXISTS (
           SELECT 1
           FROM pg_enum e
           JOIN pg_type t ON t.oid = e.enumtypid
           WHERE t.typname = 'user_role' AND e.enumlabel = 'auditor'
       ) THEN
        ALTER TYPE user_role ADD VALUE 'auditor';
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'claim_status') THEN
        CREATE TYPE claim_status AS ENUM (
            'ready_for_assignment',
            'waiting_for_documents',
            'in_review',
            'needs_qc',
            'completed',
            'withdrawn'
        );
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'parse_status') THEN
        CREATE TYPE parse_status AS ENUM ('pending', 'processing', 'succeeded', 'failed');
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'decision_recommendation') THEN
        CREATE TYPE decision_recommendation AS ENUM (
            'approve',
            'reject',
            'manual_review',
            'need_more_evidence'
        );
    END IF;
END $$;

CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    legacy_user_id BIGINT UNIQUE,
    username VARCHAR(60) NOT NULL UNIQUE,
    password_hash VARCHAR(255) NOT NULL,
    role user_role NOT NULL,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS user_bank_details (
    id BIGSERIAL PRIMARY KEY,
    user_id BIGINT NOT NULL UNIQUE REFERENCES users(id) ON DELETE CASCADE,
    account_holder_name VARCHAR(255) NOT NULL DEFAULT '',
    bank_name VARCHAR(255) NOT NULL DEFAULT '',
    branch_name VARCHAR(255) NOT NULL DEFAULT '',
    account_number VARCHAR(64) NOT NULL DEFAULT '',
    payment_rate VARCHAR(64) NOT NULL DEFAULT '',
    ifsc_code VARCHAR(32) NOT NULL DEFAULT '',
    upi_id VARCHAR(255) NOT NULL DEFAULT '',
    notes TEXT NOT NULL DEFAULT '',
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_by VARCHAR(100) NOT NULL DEFAULT '',
    updated_by VARCHAR(100) NOT NULL DEFAULT '',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS auth_logs (
    id BIGSERIAL PRIMARY KEY,
    legacy_auth_log_id BIGINT,
    user_id BIGINT NULL REFERENCES users(id) ON DELETE SET NULL,
    username VARCHAR(60) NOT NULL,
    role user_role NOT NULL,
    ip_address VARCHAR(45) NOT NULL,
    user_agent VARCHAR(255) NOT NULL,
    success BOOLEAN NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS user_sessions (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    token_hash CHAR(64) NOT NULL UNIQUE,
    role_snapshot user_role NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    expires_at TIMESTAMPTZ NOT NULL,
    revoked_at TIMESTAMPTZ
);

CREATE TABLE IF NOT EXISTS claims (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    external_claim_id VARCHAR(100) NOT NULL UNIQUE,
    patient_name VARCHAR(255),
    patient_identifier VARCHAR(100),
    status claim_status NOT NULL DEFAULT 'waiting_for_documents',
    assigned_doctor_id VARCHAR(100),
    priority SMALLINT NOT NULL DEFAULT 3,
    source_channel VARCHAR(100),
    tags JSONB NOT NULL DEFAULT '[]'::jsonb,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS claim_documents (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    legacy_document_id BIGINT,
    claim_id UUID NOT NULL REFERENCES claims(id) ON DELETE CASCADE,
    storage_key TEXT NOT NULL,
    file_name TEXT NOT NULL,
    mime_type VARCHAR(100) NOT NULL,
    file_size_bytes BIGINT,
    checksum_sha256 CHAR(64),
    parse_status parse_status NOT NULL DEFAULT 'pending',
    page_count INT,
    retention_class VARCHAR(50) NOT NULL DEFAULT 'standard',
    uploaded_by VARCHAR(100),
    uploaded_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    parsed_at TIMESTAMPTZ,
    metadata JSONB NOT NULL DEFAULT '{}'::jsonb
);

CREATE TABLE IF NOT EXISTS document_extractions (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    claim_id UUID NOT NULL REFERENCES claims(id) ON DELETE CASCADE,
    document_id UUID NOT NULL REFERENCES claim_documents(id) ON DELETE CASCADE,
    extraction_version VARCHAR(50) NOT NULL,
    model_name VARCHAR(100) NOT NULL,
    extracted_entities JSONB NOT NULL,
    evidence_refs JSONB NOT NULL DEFAULT '[]'::jsonb,
    confidence NUMERIC(5,4),
    created_by VARCHAR(100) NOT NULL DEFAULT 'system',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (document_id, extraction_version)
);

CREATE TABLE IF NOT EXISTS decision_results (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    legacy_analysis_id BIGINT,
    claim_id UUID NOT NULL REFERENCES claims(id) ON DELETE CASCADE,
    extraction_id UUID REFERENCES document_extractions(id) ON DELETE SET NULL,
    rule_version VARCHAR(50) NOT NULL,
    model_version VARCHAR(50),
    fraud_risk_score NUMERIC(5,4),
    qc_risk_score NUMERIC(5,4),
    consistency_checks JSONB NOT NULL DEFAULT '[]'::jsonb,
    rule_hits JSONB NOT NULL DEFAULT '[]'::jsonb,
    explanation_summary TEXT,
    recommendation decision_recommendation NOT NULL,
    route_target VARCHAR(100) NOT NULL,
    manual_review_required BOOLEAN NOT NULL DEFAULT TRUE,
    review_priority SMALLINT NOT NULL DEFAULT 3,
    decision_payload JSONB NOT NULL,
    generated_by VARCHAR(100) NOT NULL DEFAULT 'system',
    generated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    is_active BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE IF NOT EXISTS report_versions (
